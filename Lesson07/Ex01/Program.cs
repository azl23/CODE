int count = 0;

void PrintImage(int[,] image, int x, int y)
{
    Console.Clear();
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (i == x && j == y) Console.Write("@");
            else if (image[i, j] == 0) Console.Write(" ");
            else Console.Write("+");
        }
        Console.WriteLine();
    }
    Console.SetCursorPosition(26, 4);
    Console.WriteLine($"row: {x}   col: {y}");
}

void FillImage(int row, int col)
{
    count++;
    //PrintImage(pic, row, col);
    //Console.ReadLine();
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}


